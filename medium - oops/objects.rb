# String
puts "Hello World" # String
puts "Hello World".class # Class of the object
puts "Hello World".length # Length of the string
puts "Hello World".reverse # Reverse the string
puts "Hello World".upcase # Uppercase the string
puts "Hello World".downcase # Lowercase the string

# Integer
puts 10 # Integer
puts 10.class # Class of the object
puts 10.next # Next number
puts 10.upto(5) # Range of numbers
puts 10.succ # Successor
puts 10.pred # Predecessor